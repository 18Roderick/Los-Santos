          <li>Queso blanco molido, para servir. En Panamá se usa un queso blanco nacional, muy rico. 
            Es parecido al queso feta, aunque su textura es distinta.</li>
        </ul>
        <p>
          <b>Procedimiento</b>
          <br>
          Lavamos los nances y separamos la pulpa de la semilla y la piel del nance.
          Esto puede hacerse a mano, vaciando la botella en un colador y añadiendo una 
          taza de agua. Con las manos exprimimos los frutos para que pase la mayor cantidad
          de pulpa, luego añadimos un poco más de agua y volvemos a pasarla para aprovecharla
          al máximo. También puede hacerse echando los nances y el agua en una batidora y 
          dándole un par de pulsaciones cortas para separar la 
          pulpa sin romper las semillas y luego lo colamos, añadiendo más agua si hiciera falta.
          <br><br>
          El jugo resultante es muy concentrado, 
          ahora podemos probarlo para decidir si necesitaremos más o menos azúcar.
          <br><br>
          Añadimos otra taza de agua y disolvemos en este jugo la harina o maicena y 
          removemos para que no queden grumos (si queda alguno rebelde por allí, volvemos a colar).
          Lo llevamos al fuego y removemos hasta que espese, entonces
          añadimos poco a poco el azúcar, una taza más de agua, la sal y la canela al gusto.
          <br><br>
          Seguimos cocinando a fuego lento, removiendo con frecuencia para que no se pegue
          hasta que alcance una textura similar a las natillas, unos 20 minutos.
          <br><br>
          Servimos en cuencos o copas individuales con un poco de queso blanco molido por encima.
          <br><br>
          La pesada de nance se puede tomar caliente o fría.
          <br><br>
          ¡Que aproveche!

